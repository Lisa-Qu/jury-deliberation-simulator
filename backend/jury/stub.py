class StubLLM:
    def __init__(self, retriever, lang: str = "en"):
        self.retriever = retriever
        self.lang = lang
        self.errors: list[dict] = []

    @property
    def _zh(self) -> bool:
        return self.lang == "zh"

    def _lines(self, juror):
        return _LINES.get(juror.persona.id, _DEFAULT)

    def think(self, juror, state, case):
        n, a = juror.persona.name, juror.persona.archetype
        return f"{n}（{a}）正在掂量手头的证据。" if self._zh else f"{n} ({a}) weighs the evidence at hand."

    def speak(self, juror, state, case, on_tool_call, on_tool_result):
        L = self._lines(juror)
        on_tool_call(L["q_zh"] if self._zh else L["q_en"])
        on_tool_result(self.retriever.lookup(f'{L["q_zh"]} {L["q_en"]}', k=2))
        return (L["say_zh"] if self._zh else L["say_en"]), juror.vote

    def respond(self, juror, state, case, target_name, target_text,
                on_tool_call, on_tool_result):
        L = self._lines(juror)
        on_tool_call(L["q_zh"] if self._zh else L["q_en"])
        on_tool_result(self.retriever.lookup(f'{L["q_zh"]} {L["q_en"]}', k=1))
        if self._zh:
            return f"（回应 {target_name}）{L['reb_zh']}", juror.vote
        return f"(to {target_name}) {L['reb_en']}", juror.vote

    def revote(self, juror, state, case):
        return juror.vote, ("维持原立场" if self._zh else "holding my position")

    def hint(self, state, case):
        return ("抓住 9 点指纹匹配与实验室 12 点标准之间的差距。" if self._zh
                else "Press the gap between the 9-point print match and the lab's 12-point standard.")

    def generate_personas(self, case, n):
        return []

    def judge(self, statement, case):
        # Deterministic offline score: statements that cite concrete figures
        # (e.g. "9 point", "12-point", "4 miles") read as more evidence-grounded.
        q = 0.6 + (0.2 if any(c.isdigit() for c in statement) else 0.0)
        return {"quality": round(min(q, 0.9), 3), "fallacy": "none"}

    def rubric(self, case, human_lines, state):
        dims = {k: 72 for k in
                ["persuasiveness", "evidence_use", "consistency",
                 "engagement", "open_mindedness"]}
        recap = "离线演示评议已完成。" if self._zh else "Offline demo deliberation completed."
        return {"dims": dims, "total": 72, "recap": recap}

    def drain_errors(self):
        errs, self.errors = self.errors, []
        return errs
