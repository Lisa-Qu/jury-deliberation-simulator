# CJK chars are tokenized individually (Chinese has no spaces); latin words and
# digit runs as whole tokens. Without the CJK class, Chinese text yields ZERO
# tokens → zero vectors → meaningless retrieval.
_TOKEN_RE = re.compile(r"[a-z]+|[0-9]+|[一-鿿]")


def offline_embed(text: str, dim: int = 512):
    v = np.zeros(dim)
    for tok in _TOKEN_RE.findall(text.lower()):
        v[hash(tok) % dim] += 1.0
    return v


